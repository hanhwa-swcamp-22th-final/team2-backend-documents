package com.team2.documents.command.application.service;

import static org.junit.jupiter.api.Assertions.assertThrows;
import static org.mockito.Mockito.verify;
import static org.mockito.Mockito.when;

import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.extension.ExtendWith;
import org.mockito.InjectMocks;
import org.mockito.Mock;
import org.mockito.junit.jupiter.MockitoExtension;

import com.team2.documents.command.domain.entity.PurchaseOrder;
import com.team2.documents.command.domain.entity.enums.PurchaseOrderStatus;
import com.team2.documents.command.domain.repository.CommercialInvoiceRepository;
import com.team2.documents.command.domain.repository.PackingListRepository;
import com.team2.documents.command.domain.repository.ShipmentOrderRepository;
import com.team2.documents.command.application.service.PurchaseOrderCommandService;

@ExtendWith(MockitoExtension.class)
class PurchaseOrderDocumentGenerationServiceTest {

    @Mock
    private PurchaseOrderCommandService purchaseOrderCommandService;

    @Mock
    private CommercialInvoiceRepository commercialInvoiceRepository;

    @Mock
    private PackingListRepository packingListRepository;

    @Mock
    private ShipmentOrderRepository shipmentOrderRepository;

    @InjectMocks
    private PurchaseOrderDocumentGenerationService purchaseOrderDocumentGenerationService;

    @Test
    @DisplayName("확정 상태의 PO는 CI, PL, 출하지시서를 자동 생성한다")
    void generateDocuments_whenPurchaseOrderIsConfirmed_thenCreatesAutoGeneratedDocuments() {
        // given
        String poId = "PO2025-0001";
        PurchaseOrder purchaseOrder = new PurchaseOrder(poId, PurchaseOrderStatus.CONFIRMED);

        when(purchaseOrderCommandService.findById(poId)).thenReturn(purchaseOrder);

        // when
        purchaseOrderDocumentGenerationService.generateOnConfirmation(poId);

        // then
        verify(commercialInvoiceRepository).createFromPurchaseOrder(poId);
        verify(packingListRepository).createFromPurchaseOrder(poId);
        verify(shipmentOrderRepository).createFromPurchaseOrder(poId);
    }

    @Test
    @DisplayName("PO 정보가 없으면 자동 생성 시 예외가 발생한다")
    void generateDocuments_whenPurchaseOrderDoesNotExist_thenThrowsException() {
        // given
        String poId = "PO2025-9999";
        when(purchaseOrderCommandService.findById(poId))
                .thenThrow(new IllegalArgumentException("PO 정보를 찾을 수 없습니다."));

        // when & then
        assertThrows(IllegalArgumentException.class,
                () -> purchaseOrderDocumentGenerationService.generateOnConfirmation(poId));
    }

    @Test
    @DisplayName("확정 상태가 아닌 PO는 자동 생성할 수 없다")
    void generateDocuments_whenPurchaseOrderIsNotConfirmed_thenThrowsException() {
        // given
        String poId = "PO2025-0001";
        PurchaseOrder purchaseOrder = new PurchaseOrder(poId, PurchaseOrderStatus.APPROVAL_PENDING);

        when(purchaseOrderCommandService.findById(poId)).thenReturn(purchaseOrder);

        // when & then
        assertThrows(IllegalStateException.class,
                () -> purchaseOrderDocumentGenerationService.generateOnConfirmation(poId));
    }
}
